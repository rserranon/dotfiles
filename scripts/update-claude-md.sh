### Code Patterns

- **Lua plugins**: return a single table; use `opts =` (not `config =`) when possible; declare keybinds in `keys =`
- **Shell scripts**: `set -euo pipefail`; colored output via `pass()`/`fail()`/`warn()` functions
- **Aliases**: grouped by domain with `# comment` headers; no spaces around `=`
- **Stow packages**: double-nested (`pkg/pkg/file`); never put files directly at package root
- **JSON**: ignored globally (`*.json`) with `!claude/**/*.json` exception for Claude configs
- **Secrets**: always guarded with `[[ -f path ]] && source path`; never committed

