# scripts/update-claude-md.sh
# Regenerates CLAUDE.md from live repo analysis + static guidance.
# Run after adding tools, changing configs, or when CLAUDE.md feels stale.
set -euo pipefail

DOTFILES_DIR="$(cd "$(dirname "$0")/.." && pwd)"
OUTPUT="$DOTFILES_DIR/CLAUDE.md"
cd "$DOTFILES_DIR"

GENERATED_AT="$(date +%Y-%m-%d)"
BREW_F="$(grep -c '^brew ' homebrew/Brewfile 2>/dev/null || echo 0)"
BREW_C="$(grep -c '^cask ' homebrew/Brewfile 2>/dev/null || echo 0)"

recent_commits() {
  git log --pretty=format:"| \`%h\` | %ad | %s |" --date=short -10
}

hot_files() {
  git log --format="" --name-only | grep -v '^$' | sort | uniq -c | sort -rn | head -10 \
    | awk '{printf "| %-45s | %s changes |\n", $2, $1}'
}

commit_types() {
  git log --pretty=format:"%s" \
    | grep -E '^(feat|fix|chore|docs|refactor|test|style|perf):' \
    | sed 's/:.*//' | sort | uniq -c | sort -rn \
    | awk '{printf "  - **%s**: %d commits\n", $2, $1}'
}

nvim_plugins() {
  ls nvim/nvim/lua/plugins/*.lua 2>/dev/null | while read -r f; do
    printf '  - `%s.lua`\n' "$(basename "$f" .lua)"
  done
}

